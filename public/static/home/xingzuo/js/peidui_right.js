function tool_match() {
    var tool_match_sign_1 = $.cookie("match_sign_1") ? parseInt($.cookie("match_sign_1")) : 6;
    var tool_match_sign_2 = $.cookie("match_sign_2") ? parseInt($.cookie("match_sign_2")) : 10;

    var match_sign_1 = $("#TOOL_MATCH_SIGN_1");
    var match_sign_2 = $("#TOOL_MATCH_SIGN_2");

    //默认星座
    var xz_nan = Get_Sign_By_ID(tool_match_sign_1);
    var xz_nv = Get_Sign_By_ID(tool_match_sign_2);
    var match_sign_nan = $("#TOOL_MATCH_SIGN_NAN");
    var match_sign_nv = $("#TOOL_MATCH_SIGN_NV");

    $('#TOOL_MATCH_SIGN_1 input[name="MATCH_SIGN_1"]').val(tool_match_sign_1)
    $('#TOOL_MATCH_SIGN_2 input[name="MATCH_SIGN_2"]').val(tool_match_sign_2)
    match_sign_1.find("div :first").html(xz_nan.ChineseName.substring(0, 2) + "男");
    match_sign_2.find("div :first").html(xz_nv.ChineseName.substring(0, 2) + "女");

    match_sign_nan.removeClass().addClass("tool_sign_logo nan s_" + tool_match_sign_1);
    match_sign_nv.removeClass().addClass("tool_sign_logo s_" + tool_match_sign_2);

    $("#TOOL_MATCH_SIGN_1").find("span").click(function () {
        match_sign_nan.removeClass().addClass("tool_sign_logo nan s_" + $(this).attr("val"));
        $.cookie("match_sign_1", $(this).attr("val"), { expires: 365, path: '/' });
    });
    $("#TOOL_MATCH_SIGN_2").find("span").click(function () {
        match_sign_nv.removeClass().addClass("tool_sign_logo s_" + $(this).attr("val"));
        $.cookie("match_sign_2", $(this).attr("val"), { expires: 365, path: '/' });
    });
}